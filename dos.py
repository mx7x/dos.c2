url = input(Fore.CYAN + "[?] Enter target URL (https://example.com): ").strip()
method = input(Fore.CYAN + "[?] HTTP method (GET or POST): ").strip().upper()
threads_count = int(input(Fore.CYAN + "[?] Number of threads: "))
requests_per_thread = int(input(Fore.CYAN + "[?] Requests per thread: "))

def send_requests():
    for _ in range(requests_per_thread):
        try:
            if method == "GET":
                response = requests.get(url)
            elif method == "POST":
                response = requests.post(url, data={"test": "data"})
            else:
                print(Fore.RED + "[!] Invalid method.")
                break
            print(Fore.GREEN + f"[✓] {method} {url} => {response.status_code}")
        except Exception as e:
            print(Fore.RED + f"[✗] Error: {e}")

print(Fore.YELLOW + "\n[*] Starting HTTP load test...\n")
start_time = time.time()

threads = []
for _ in range(threads_count):
    t = threading.Thread(target=send_requests)
    t.start()
    threads.append(t)

for t in threads:
    t.join()

end_time = time.time()
print(Fore.MAGENTA + f"\n[✓] Test finished in {end_time - start_time:.2f} seconds.")
